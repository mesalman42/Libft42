#include "libft.h"

static int	num_len(long n)
{
	int	len;

	len = 0;
	if (n <= 0)
		len = 1;
	while (n)
	{
		n /= 10;
		len++;
	}
	return (len);
}

char	*ft_itoa(int n)
{
	char	*str;
	long	nb;
	int		len;
	int		neg;

	nb = n;
	neg = (nb < 0);
	if (neg)
		nb = -nb;
	len = num_len(n);
	str = malloc(len + 1);
	if (!str)
		return (NULL);
	str[len] = '\0';
	while (len--)
	{
		str[len] = '0' + (nb % 10);
		nb /= 10;
	}
	if (neg)
		str[0] = '-';
	return (str);
}
